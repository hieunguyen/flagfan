	public int eval(Position p) {
		nodeCount++;
		int e = 0;
		for (int i=16; i<48; i++) {
			int sqr = p.pieces[i];
			if (p.board[sqr]!=0) {
				int pt = PIECE_TYPES[i];
				int pieceValue = i<32 ? POS_VALUES[pt][sqr] : POS_VALUES[pt][254-sqr];
				if (p.turnAtRoot==RED^i>=32) e += pieceValue; else e -= pieceValue;
			}
		}
		return e;
	}
	
